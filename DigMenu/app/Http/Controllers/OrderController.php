    {       
        $logger =new Logger; 

        try{
            $order = new Order;
            $order->client_name =  $request->client_name;
            $order->plats= json_encode($request->json_plats);
            $order->user_id = $request->user_id;
            $order->save();
         }
         catch(\Exception $e){
            // do task when error
            
            $logger->logError( $e->getMessage());
              // insert query
         }

        return $order->id;
    
    }

